// This program displays area and perimeter of a rectangle in c++

#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "Length 7cm and width 4cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 7 * 4 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (7 + 4) << " cm\n";
}
